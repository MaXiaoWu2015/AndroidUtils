public class Entity {
    private String mTitle;
    private String mContent;
    public Entity(String pTitle, String pContent) {
        mTitle = pTitle;
        mContent = pContent;
    }
    public String getTitle() {
        return mTitle;
    }
    public String getContent() {
        return mContent;
    }
}
